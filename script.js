// Відображення випадаючого списку
function showDropdown() {
  const dropdown = document.getElementById("dropdown");
  dropdown.style.display = "block";
  updateDropdown("");
}

// Оновлення списку при введенні
function filterCountries() {
  const searchValue = document
    .getElementById("country-search")
    .value.toLowerCase();
  updateDropdown(searchValue);
}

// Заповнення випадаючого списку
function updateDropdown(searchValue) {
  const dropdown = document.getElementById("dropdown");
  dropdown.innerHTML = "";

  Object.keys(countryData).forEach((country) => {
    if (country.toLowerCase().includes(searchValue)) {
      const div = document.createElement("div");
      const img = document.createElement("img");
      img.src = countryData[country].flag;
      div.appendChild(img);

      const text = document.createTextNode(country);
      div.appendChild(text);

      div.onclick = () => {
        document.getElementById("country-search").value = country;
        dropdown.style.display = "none";
        changeContent(country);
      };
      dropdown.appendChild(div);
    }
  });
}

// Змінення контенту при виборі країни
function changeContent(country) {
  if (!countryData[country]) return;
  document.getElementById("video-frame").src = countryData[country].video;
  document.getElementById("singer-name").textContent =
    "Виконавець: " + countryData[country].singer;
  document.getElementById("song-info").textContent =
    "Пісня: " + countryData[country].song;
  document.getElementById("lyrics").textContent =
    "Переклад: " + countryData[country].lyrics;
}
